def crear_tarjeta(nombre, mensaje="¡Felicidades!", ocasion="cumpleaños"):
    print("************************")
    print(f"Para: {nombre}")
    print(f"Ocasión: {ocasion}")
    print(mensaje)
    print("************************\n")

crear_tarjeta("Ana")
crear_tarjeta("Carlos", "¡Muchos éxitos!", "graduación")
